package com.chenzhi.module.domain;

public class ModuleData {

    private  double failNum;
    private  double successNum;
    private  double sunNum;
    private  String moduleNmae;

    public double getFailNum() {
        return failNum;
    }

    public void setFailNum(double failNum) {
        this.failNum = failNum;
    }

    public double getSuccessNum() {
        return successNum;
    }

    public void setSuccessNum(double successNum) {
        this.successNum = successNum;
    }

    public double getSunNum() {
        return sunNum;
    }

    public void setSunNum(double sunNum) {
        this.sunNum = sunNum;
    }

    public String getModuleNmae() {
        return moduleNmae;
    }

    public void setModuleNmae(String moduleNmae) {
        this.moduleNmae = moduleNmae;
    }
}
